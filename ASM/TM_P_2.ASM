	CALL		DELAY_SHORT
	CALL		I2C_START_EE
	CALL		DELAY_SHORT
	MOV			A,#EER_ADD
	MOV			DATA_IN,A
	CALL		EEADDRESS_ASK
	B0BCLR		SDA_CONT
	BCLR		ASK_FLAG
	CALL		EEDATA_ASK
	MOV			A,DATA_OUT
	MOV			ADDRESSH,A
	B0BCLR		SDA_CONT
	CALL		DELAY_MINI
	BSET		ASK_FLAG
	CALL		EEDATA_ASK
	MOV			A,DATA_OUT
	MOV			ADDRESSL,A
	CALL		I2C_END_EE
;*************************************
	CALL		DELAY_SHORT
	B0BCLR		SCL_CONT
	RET
;************************************************
I2C_EEROR:
	;open interrupt
	;SleepMode
	BCLR	I2CERROR_FLAG
	BTS0	VTM_IO
	BSET	TM_I2C_EEROR
	RET
I2C_START_EE:
	CALL		DELAY_MINI
	B0BCLR 		SDA_IO
	CALL 		DELAY_MINI
	B0BCLR 		SCL_IO
	CALL 		DELAY_MINI
	RET
I2C_END_EE:
	B0BCLR 		SDA_IO
	CALL 		DELAY_MINI
	B0BSET 		SCL_IO
	CALL 		DELAY_MINI
	B0BSET 		SDA_IO
	CALL		DELAY_MINI
	RET
I2C_START_TM:
	CALL		DELAY_MINI
	B0BCLR 		SDATA_IO
	CALL 		DELAY_MINI
	B0BCLR 		SCL_IO
	CALL 		DELAY_MINI
	RET
I2C_END_TM:
	B0BCLR 		SDATA_IO
	CALL 		DELAY_MINI
	B0BSET 		SCL_IO
	CALL 		DELAY_MINI
	B0BSET 		SDATA_IO
	CALL		DELAY_MINI
	RET
TMADDRESS_ASK:
	MOV 		A,#8
	MOV 		TIMEOUT,A
TMADDRESS_LOOP:
	BTS0		DATA_IN.7
	JMP 		MD_1
	B0BTS0 		SDATA_IO
	B0BCLR		SDATA_IO
	JMP 		NEXT_1
MD_1:
	B0BTS1 		SDATA_IO
	B0BSET		SDATA_IO
NEXT_1:
	CALL		DELAY_MINI
	B0BSET 		SCL_IO
	CALL		DELAY_MINI
	RLCM 		DATA_IN
	B0BCLR 		SCL_IO
	DECMS 		TIMEOUT
	JMP 		TMADDRESS_LOOP
	
	B0BCLR 		SDATA_CONT
	CALL		DELAY_MINI
	B0BSET 		SCL_IO
	CALL 		CALL_DELAY
	B0BTS0 		SDATA_IO
	CALL		I2C_EEROR
	CALL		DELAY_MINI
	BCLR 		SCL_IO
	BSET 		SDATA_CONT
	RET
;**********************TM_READING_DATA********************************
TMDATA_ASK:
	B0BCLR 		SDATA_CONT
	MOV 		A,#8
	MOV 		TIMEOUT,A
TMDATA_LOOP:
	MOV 		A,#9
	MOV 		TLC0,A
	CALL		DELAY_MINI
	BSET 		SCL_IO
	CALL 		CALL_DELAY
	B0BTS0 		SDATA_IO
	BSET 		DATA_OUT.0
	B0BTS1 		SDATA_IO
	BCLR 		DATA_OUT.0
	RLCM 		DATA_OUT
	CALL		DELAY_MINI
	B0BCLR 		SCL_IO
	DECMS 		TIMEOUT
	JMP 		TMDATA_LOOP
	B0BSET 		SDATA_CONT
	BTS0 		ASK_FLAG
	B0BSET 		SDATA_IO
	BTS1 		ASK_FLAG
	B0BCLR 		SDATA_IO
	RRCM 		DATA_OUT
	CALL		DELAY_MINI
	BSET 		SCL_IO
	CALL		DELAY_MINI
	BCLR		SCL_IO	
	RET
;******************************************************
EEADDRESS_ASK:
	MOV 		A,#8
	MOV 		TIMEOUT,A
EEADDRESS_LOOP:
	BTS0		DATA_IN.7
	JMP 		EEMD_1
	B0BTS0 		SDA_IO
	B0BCLR		SDA_IO
	JMP 		EENEXT_1
EEMD_1:
	B0BTS1 		SDA_IO
	B0BSET		SDA_IO
EENEXT_1:
	CALL		DELAY_MINI
	B0BSET 		SCL_IO
	CALL		DELAY_MINI
	RLCM 		DATA_IN
	B0BCLR 		SCL_IO
	DECMS 		TIMEOUT
	JMP 		EEADDRESS_LOOP
	
	B0BCLR 		SDA_CONT
	CALL		DELAY_MINI
	B0BSET 		SCL_IO
	CALL 		CALL_DELAY
	B0BTS0 		SDA_IO
	CALL		I2C_EEROR
	CALL		DELAY_MINI
	BCLR 		SCL_IO
	BSET 		SDA_CONT
	RET
;****************************************
EEDATA_ASK:
	B0BCLR 		SDA_CONT
	MOV 		A,#8
	MOV 		TIMEOUT,A
EEDATA_LOOP:
	MOV 		A,#9
	MOV 		TLC0,A
	CALL		DELAY_MINI
	BSET 		SCL_IO
	CALL 		CALL_DELAY
	B0BTS0 		SDA_IO
	BSET 		DATA_OUT.0
	B0BTS1 		SDA_IO
	BCLR 		DATA_OUT.0
	RLCM 		DATA_OUT
	CALL		DELAY_MINI
	B0BCLR 		SCL_IO
	DECMS 		TIMEOUT
	JMP 		EEDATA_LOOP
	
	B0BSET 		SDA_CONT
	BTS0 		ASK_FLAG
	B0BSET 		SDA_IO
	BTS1 		ASK_FLAG
	B0BCLR 		SDA_IO
	RRCM 		DATA_OUT
	CALL		DELAY_MINI
	BSET 		SCL_IO
	CALL		DELAY_MINI
	BCLR		SCL_IO	
	RET
